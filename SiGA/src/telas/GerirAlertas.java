    private void jBCriarNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCriarNovoActionPerformed
        // TODO add your handling code here:
        CadastrarAlerta cadastrarAlerta = new CadastrarAlerta(this.owner, "Criar Alerta", rootPaneCheckingEnabled
        );
        //this.setEnabled(false);
        cadastrarAlerta.setVisible(true);

        if (cadastrarAlerta.getAlerta() != null) {
            if (alertas == null) {
                alertas = new ArrayList<Alerta>();
            }
            Alerta alertaCriado = cadastrarAlerta.getAlerta();
            alertaCriado.setLoginUsuario(usuario.getLogin());
            alertas.add(cadastrarAlerta.getAlerta());
        }

        salvarAlertas(alertas);
        atualizaAlertas();
    }//GEN-LAST:event_jBCriarNovoActionPerformed

    private void jBApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBApagarActionPerformed
        // TODO add your handling code here:
        if (alertas.size() > 0) {
            alertas.remove(jCBAlertas.getSelectedIndex());
            salvarAlertas(alertas);
            atualizaAlertas();
        }
    }//GEN-LAST:event_jBApagarActionPerformed

